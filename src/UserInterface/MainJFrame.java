    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // Get user name
        String userName = userNameJTextField.getText();
        // Get Password
        char[] passwordCharArray = passwordField.getPassword();
        String password = String.valueOf(passwordCharArray);
        System.out.println("-----0.1------------");
        //Step1: Check in the system user account directory if you have the user
        userAccount = system.getUserAccountDirectory().authenticateUser(userName, password);
        Enterprise inEnterprise = null;
        Organization inOrganization = null;
        System.out.println("-----0.2------------");
        if (userAccount == null) {
            //Step2: Go inside each network to check each enterprise
            NetworkDirectory networkDirectory = system.getNetworkList();
            for (Network network : networkDirectory.getNetworkList()) {
                System.out.println("-----0.21------------");
                //Step 2-a: Check against each enterprise
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterPriseList()) {
                    userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                    System.out.println("-----0.22------------");
                    if (userAccount == null) {
                        System.out.println("-----0.23------------");
                        //Step3: Check against each organization inside that enterprise
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                            System.out.println("-----0.24------------");
                            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if (userAccount != null) {
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                System.out.println("-----0.3------------");
                                break;
                            }
                        }
                    } else {
                        inEnterprise = enterprise;
                        break;
                    }
                    if (inOrganization != null) {
                        break;
                    }
                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }
        System.out.println("-----1.1------------");
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        if (userAccount == null) {
            person = system.getPersonDirectory().authenticateUser(userName, password);
            System.out.println("-----1.3------------");
            if (person != null) {
                System.out.println("===== above the patient work area =======");
                String activity = person.getUsername() + "," + "logged in," + (new Date());
                GenerateCSV.createloginlogs(activity);
                userProcessContainer.add("workArea", new PersonViewAndTakeAppointment(userProcessContainer, inEnterprise, person, system));
                layout.next(userProcessContainer);
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Credentails!");
                return;
            }

        } else {
            Container container;

            System.out.println("-----1.3------------");
            if (userAccount.getUserAccountStatus().getValue().equalsIgnoreCase("Active")) {
                String activity = userAccount.getUsername() + "," + "logged in," + (new Date());
                GenerateCSV.createloginlogs(activity);
                userProcessContainer.add("workArea", userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, system));
                layout.next(userProcessContainer);
            } else {
                JOptionPane.showMessageDialog(null, "your Account is not Active, Please check with Admin Department");
                return;
            }

        }
        loginJButton.setEnabled(false);
        logoutJButton.setEnabled(true);
        userNameJTextField.setEnabled(false);
        passwordField.setEnabled(false);
        userSignupbtn.setEnabled(false);
    }//GEN-LAST:event_loginJButtonActionPerformed

