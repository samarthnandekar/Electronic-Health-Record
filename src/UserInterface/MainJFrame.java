    private void logoutJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutJButtonActionPerformed
        logoutJButton.setEnabled(false);
        userNameJTextField.setEnabled(true);
        passwordField.setEnabled(true);
        loginJButton.setEnabled(true);
        userSignupbtn.setEnabled(true);
        userNameJTextField.setText("");
        passwordField.setText("");

        if (userAccount != null) {
            String activity = userAccount.getUsername() + "," + "logged out," + (new Date());
            GenerateCSV.createloginlogs(activity);
        }

        if (person != null) {
            String activity = person.getUsername() + "," + "logged out," + (new Date());
            GenerateCSV.createloginlogs(activity);
        }

        userProcessContainer.removeAll();
        HomePage homePage = new HomePage();
        userProcessContainer.add("blank", homePage);
        CardLayout crdLyt = (CardLayout) userProcessContainer.getLayout();
        crdLyt.next(userProcessContainer);
        dB4OUtil.storeSystem(system);
    }//GEN-LAST:event_logoutJButtonActionPerformed

